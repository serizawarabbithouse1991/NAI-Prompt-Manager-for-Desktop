export type TranslationKey = keyof typeof translations.ja

export function translate(language: AppLanguage, key: TranslationKey, vars?: Vars): string {
  const template = translations[language]?.[key] ?? translations.ja[key] ?? key
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, name: string) =>
    vars[name] === undefined ? `{${name}}` : String(vars[name])
  )
}

export function useI18n() {
  const language = useAppStore((state) => state.settings.language)
  return {
    language,
    t: (key: TranslationKey, vars?: Vars) => translate(language, key, vars),
  }
}
